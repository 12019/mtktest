void RDA_bt_Power_On_Reset(void)
{   

   GPIO_WriteIO(0,RDA5868_RESET); 
   RDABT_DELAY(20);   
   GPIO_WriteIO(1,RDA5868_RESET);    
}

void RDA_bt_Power_Off(void)
{

   //RDA_bt_SerialCommCryClkOff();
   GPIO_WriteIO(0,RDA5868_RESET);    
   GPIO_WriteIO(0,RDA5868_POWER_EN);    

   
}

#if defined( __RDA_AUTO_CHIP__) && defined(__RDA5868_USE_IIC__)

kal_uint8 rdabt_get_chipid(void)
{
	rda5868_read_data(chipidaddr,&rda5868_chipid,1);
    if(((rda5868_chipid>>16)&0x000000ff) == 0x11 && (rda5868_chipid & 0x00ffff) == 0x5868) 
        return RDA_5868_R7;
    else if((((rda5868_chipid>>16)&0x000000ff) == 0x03 || ((rda5868_chipid>>16)&0x000000ff) == 0x01 ) && (rda5868_chipid & 0x0000ffff) == 0x5868)
        return RDA_5868_R5;
	else
		return RDA_NONE_CHIP;		
}

#endif

   
void rda_bt_power_on(void)
{
   GPIO_ModeSetup(RDA5868_POWER_EN,0);
   GPIO_InitIO(1,RDA5868_POWER_EN);
   GPIO_WriteIO(1,RDA5868_POWER_EN); 
   RDABT_DELAY(1);   
   GPIO_ModeSetup(RDA5868_RESET,0);
   GPIO_InitIO(1,RDA5868_RESET);
   GPIO_WriteIO(1,RDA5868_RESET);    
   RDABT_DELAY(20);   
#ifdef __RDA5868_USE_IIC__   
   RDA_bt_SerialCommInit(); 
   RDABT_DELAY(1);
#endif
#ifdef __RDA_SOFT_RESET__  
   rdabt_sw_reset_write();
   RDA_bt_Power_Off();
   RDABT_DELAY(10);
   GPIO_WriteIO(1,RDA5868_POWER_EN); 
   RDABT_DELAY(4);   
   GPIO_WriteIO(1,RDA5868_RESET);    
   RDABT_DELAY(10);      
#endif   
   RDA_bt_Power_On_Reset();
   RDABT_DELAY(70);        
#ifdef __RDA5868_USE_IIC__

#if defined( __RDA_AUTO_CHIP__)
	   rdabt_chip_sel = rdabt_get_chipid();
	   kal_prompt_trace(0,"rdabt_chip_sel = %d",rdabt_chip_sel);

	   if(rdabt_chip_sel == RDA_5868_R7)
		   RDA5400_Intialization_2(); 
	   else if(rdabt_chip_sel == RDA_5868_R5)  
		   RDA5400_Intialization();
	   else
	   	   ASSERT(0);
#else//__RDA_AUTO_CHIP__
#ifdef __RDA_CHIP_R5__
	RDA5400_Intialization();
#endif///__RDA_CHIP_R5__
#ifdef __RDA_CHIP_R7__
	RDA5400_Intialization_2();
#endif//__RDA_CHIP_R7__
#endif///__RDA_AUTO_CHIP__
#endif//__RDA5868_USE_IIC__
   
   rda5868_uart_start();
   rda5868_uart_configure(115200);

}


